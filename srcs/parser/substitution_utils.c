#include "../../inc/minishell.h"

int			substitution_end_characters(char c)
{
	if (c == ':' || c == '.' || c == ',' || c == '-' || c == '=' || c == '+'
		|| c == '*' || c == '^' || c == '%' || c == ' ' || c == '$'
		|| c == '\\' || c == '\"' || c == '\'' || c == '[' || c == ']'
		|| c == '/' || c == '?' || c == '{' || c == '}' || c == '#'
		|| c == '@' || c == '~' || c == '`')
		return (1);
	return (0);
}
